const calculator = {
  plus: function (a, b) {
    alert(a + b);
  },
  minus: function (a, b) {
    alert(a - b);
  },
  divide: function (a, b) {
    alert(a / b);
  },
  times: function (a, b) {
    alert(a * b);
  },
  power: function (a, b) {
    alert(a ** b);
  },
};

calculator.plus(5, 2);

calculator.minus(5, 2);

calculator.divide(5, 2);

calculator.power(5, 2);
